class IconComponent extends HTMLElement {
  static get observedAttributes() {
    return ['name', 'size'];
  }

  constructor() {
    super();
    this.attachShadow({ mode: 'open' });
  }

  connectedCallback() {
    this.render();
  }

  attributeChangedCallback() {
    this.render();
  }

  render() {
    const name = this.getAttribute('name');
    const size = this.getAttribute('size') || '';
    const iconPath = ICONS[name];

    if (!iconPath) {
      console.warn(`Icon "${name}" not found`);
      this.shadowRoot.innerHTML = '';
      return;
    }

    // 根据 size 属性计算尺寸
    let dimensions = { width: 16, height: 16 };
    switch (size) {
      case 'sm': dimensions = { width: 14, height: 14 }; break;
      case 'md': dimensions = { width: 18, height: 18 }; break;
      case 'lg': dimensions = { width: 20, height: 20 }; break;
      case 'xl': dimensions = { width: 24, height: 24 }; break;
    }

    this.shadowRoot.innerHTML = `
      <style>
        :host {
          display: inline-flex;
          align-items: center;
          justify-content: center;
          flex-shrink: 0;
          width: ${dimensions.width}px;
          height: ${dimensions.height}px;
        }
        svg {
          width: 100%;
          height: 100%;
          fill: none;
          stroke: currentColor;
          stroke-width: 2;
          stroke-linecap: round;
          stroke-linejoin: round;
        }
      </style>
      <svg viewBox="0 0 24 24">${iconPath}</svg>
    `;
  }
}

// 注册 Web Component
customElements.define('icon-component', IconComponent);

// 导出图标列表供其他模块使用
window.ICONS = ICONS;
